package com.nerdvana.positiveoffline.model;

public class OtherPrinterModel {
    private String head;
    private String sub;

    public OtherPrinterModel(String head, String sub) {
        this.head = head;
        this.sub = sub;
    }

    public String getHead() {
        return head;
    }

    public String getSub() {
        return sub;
    }
}

