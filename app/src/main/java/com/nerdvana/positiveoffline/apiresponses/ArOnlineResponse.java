        @Expose
        private String image_file;
        @SerializedName("id")
        @Expose
        private Integer id;
        @SerializedName("core_id")
        @Expose
        private Integer coreId;
        @SerializedName("ar_online")
        @Expose
        private String arOnline;
        @SerializedName("created_by")
        @Expose
        private Integer createdBy;
